as $$
declare
  task_row public.homework_tasks%rowtype;
  workflow_row public.task_workflow_current%rowtype;
  review_row public.task_review_records%rowtype;
  prior_attempt public.correction_attempts%rowtype;
  attempt_id uuid;
  next_attempt_number integer;
  node_id uuid;
  evidence_level public.mastery_level;
begin
  if auth.uid() is null then raise exception 'authentication required'; end if;
  if target_idempotency_key is null then raise exception 'idempotency key required'; end if;
  if exists (select 1 from public.change_events where actor_id = auth.uid() and idempotency_key = target_idempotency_key) then
    select (after_value ->> 'attempt_id')::uuid into attempt_id
    from public.change_events where actor_id = auth.uid() and idempotency_key = target_idempotency_key;
    return attempt_id;
  end if;
  select * into task_row from public.homework_tasks where id = target_task_id and deleted_at is null;
  if task_row.id is null or not public.is_task_tutor(task_row.id) then raise exception 'subject tutor access required'; end if;
  select * into workflow_row from public.task_workflow_current where task_id = target_task_id for update;
  if workflow_row.version <> expected_version then raise exception 'version conflict'; end if;
  if workflow_row.stage not in ('awaiting_correction', 'awaiting_redo') then raise exception 'task is not awaiting correction or redo'; end if;
  select review.* into review_row from public.task_review_records review
  where review.task_id = target_task_id
    and not exists (select 1 from public.task_review_revocations revoked where revoked.review_id = review.id)
  order by review.review_number desc limit 1;
  if review_row.id is null or not review_row.correction_required then raise exception 'active correction requirement not found'; end if;
  if redo_passed and (not correction_passed or not review_row.redo_required) then raise exception 'redo result is inconsistent'; end if;
  select correction.* into prior_attempt from public.correction_attempts correction
  where correction.review_id = review_row.id
    and not exists (select 1 from public.correction_attempt_revocations revoked where revoked.correction_attempt_id = correction.id)
  order by correction.attempt_number desc limit 1;
  if prior_attempt.correction_passed and (not review_row.redo_required or prior_attempt.redo_passed) then raise exception 'correction is already accepted'; end if;
  select coalesce(max(attempt_number), 0) + 1 into next_attempt_number
  from public.correction_attempts where review_id = review_row.id;

  insert into public.correction_attempts(
    task_id, review_id, attempt_number, correction_passed, redo_required,
    redo_passed, note, validated_by, idempotency_key
  ) values (
    target_task_id, review_row.id, next_attempt_number, correction_passed,
    review_row.redo_required, redo_passed, coalesce(target_note, ''),
    auth.uid(), target_idempotency_key
  ) returning id into attempt_id;

  evidence_level := case
    when not correction_passed then 'reinforce'::public.mastery_level
    else 'basic'::public.mastery_level end;
  for node_id in
    select link.knowledge_node_id from public.task_knowledge_links link
    where link.task_id = task_row.id
  loop
    insert into public.mastery_evidence(
      student_id, subject_id, knowledge_node_id, task_id, homework_version_id,
      review_id, correction_attempt_id, evidence_type, level, detail, created_by
    ) values (
      task_row.student_id, task_row.subject_id, node_id, task_row.id,
      task_row.homework_version_id, review_row.id, attempt_id,
      case when redo_passed then 'independent_redo' else 'correction' end,
      evidence_level,
      jsonb_build_object('attempt_number', next_attempt_number, 'correction_passed', correction_passed, 'redo_passed', redo_passed),
      auth.uid()
    );
    perform public.recalculate_mastery_snapshot(node_id);
  end loop;

  update public.task_reviews
  set correction_passed = record_correction_attempt.correction_passed,
    redo_passed = record_correction_attempt.redo_passed, updated_at = now()
  where task_id = target_task_id;
  perform public.refresh_task_workflow(target_task_id);
  insert into public.change_events(
    family_id, student_id, subject_id, entity_type, entity_id, event_type,
    after_value, actor_id, idempotency_key
  ) values (
    task_row.family_id, task_row.student_id, task_row.subject_id, 'correction_attempt',
    attempt_id::text, 'correction_validated',
    jsonb_build_object('attempt_id', attempt_id, 'attempt_number', next_attempt_number,
      'correction_passed', correction_passed, 'redo_required', review_row.redo_required,
      'redo_passed', redo_passed), auth.uid(), target_idempotency_key
  );
  perform public.notify_task_audience(target_task_id, 'student',
    case when correction_passed then 'correction_accepted' else 'correction_retry' end,
    case when correction_passed then '订正验收完成' else '订正未通过，请继续修改' end,
    task_row.title);
  return attempt_id;
end;
$$;

