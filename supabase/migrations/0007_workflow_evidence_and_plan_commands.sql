  evidence_id uuid;
  current_stage public.workflow_stage;
begin
  if auth.uid() is null then raise exception 'authentication required'; end if;
  if target_idempotency_key is null then raise exception 'idempotency key required'; end if;
  if target_level = 'unpracticed' then raise exception 'unpracticed is not a tutor assessment'; end if;
  if exists (select 1 from public.change_events where actor_id = auth.uid() and idempotency_key = target_idempotency_key) then
    select (after_value ->> 'evidence_id')::uuid into evidence_id
    from public.change_events where actor_id = auth.uid() and idempotency_key = target_idempotency_key;
    return evidence_id;
  end if;
  select * into task_row from public.homework_tasks where id = target_task_id and deleted_at is null;
  if task_row.id is null or not public.is_task_tutor(task_row.id) then raise exception 'subject tutor access required'; end if;
  if not exists (
    select 1 from public.task_knowledge_links
    where task_id = target_task_id and knowledge_node_id = target_knowledge_node_id
  ) then raise exception 'knowledge node is not linked to this homework version'; end if;
  select * into workflow_row from public.task_workflow_current where task_id = target_task_id for update;
  if workflow_row.version <> expected_version then raise exception 'version conflict'; end if;
  if workflow_row.stage not in ('awaiting_acceptance', 'closed_loop') then raise exception 'task is not ready for mastery assessment'; end if;
  select review.* into review_row from public.task_review_records review
  where review.task_id = target_task_id
    and not exists (select 1 from public.task_review_revocations revoked where revoked.review_id = review.id)
  order by review.review_number desc limit 1;
  if review_row.id is null then raise exception 'active review required'; end if;

  if review_row.correction_required then
    select correction.* into correction_row from public.correction_attempts correction
    where correction.review_id = review_row.id
      and not exists (select 1 from public.correction_attempt_revocations revoked where revoked.correction_attempt_id = correction.id)
    order by correction.attempt_number desc limit 1;
  end if;
  if target_level = 'mastered' and review_row.correction_required
    and (correction_row.id is null or not correction_row.correction_passed)
  then raise exception 'correction must pass before mastery'; end if;
  if target_level = 'mastered' and review_row.redo_required
    and (correction_row.id is null or not correction_row.redo_passed)
  then raise exception 'independent redo must pass before mastery'; end if;

  insert into public.mastery_evidence_revocations(evidence_id, reason, revoked_by)
  select evidence.id, '被新的家教评级取代', auth.uid()
  from public.mastery_evidence evidence
  where evidence.task_id = target_task_id
    and evidence.knowledge_node_id = target_knowledge_node_id
    and evidence.evidence_type = 'tutor_assessment'
    and not exists (
      select 1 from public.mastery_evidence_revocations revoked
      where revoked.evidence_id = evidence.id
    );

  insert into public.mastery_evidence(
    student_id, subject_id, knowledge_node_id, task_id, homework_version_id,
    review_id, correction_attempt_id, evidence_type, level, detail, created_by
  ) values (
    task_row.student_id, task_row.subject_id, target_knowledge_node_id,
    task_row.id, task_row.homework_version_id, review_row.id, correction_row.id,
    'tutor_assessment', target_level,
    jsonb_build_object('note', coalesce(target_note, '')), auth.uid()
  ) returning id into evidence_id;
  perform public.recalculate_mastery_snapshot(target_knowledge_node_id);
  current_stage := public.refresh_task_workflow(target_task_id);
  if current_stage in ('awaiting_acceptance', 'closed_loop') and not exists (
    select 1 from public.task_knowledge_links link
    where link.task_id = task_row.id
      and not exists (
        select 1 from public.mastery_evidence evidence
        where evidence.task_id = task_row.id and evidence.knowledge_node_id = link.knowledge_node_id
          and evidence.evidence_type = 'tutor_assessment' and evidence.level = 'mastered'
          and not exists (select 1 from public.mastery_evidence_revocations revoked where revoked.evidence_id = evidence.id)
      )
  ) then
    update public.task_reviews set mastery_confirmed = true, updated_at = now() where task_id = target_task_id;
  end if;
  insert into public.change_events(
    family_id, student_id, subject_id, entity_type, entity_id, event_type,
    after_value, actor_id, idempotency_key
  ) values (
    task_row.family_id, task_row.student_id, task_row.subject_id, 'mastery_evidence',
    evidence_id::text, 'mastery_assessed',
    jsonb_build_object('evidence_id', evidence_id, 'knowledge_node_id', target_knowledge_node_id, 'level', target_level),
    auth.uid(), target_idempotency_key
  );
  return evidence_id;
end;
$$;

