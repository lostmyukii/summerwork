      and assignment.starts_at <= now() and (assignment.ends_at is null or assignment.ends_at > now());
  elsif target_audience = 'student' then
    insert into public.notifications(family_id, student_id, subject_id, recipient_id, notification_type, title, body, entity_type, entity_id)
