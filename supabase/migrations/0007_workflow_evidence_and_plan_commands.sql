create or replace function public.recalculate_mastery_snapshot(target_knowledge_node_id uuid)
returns void
language plpgsql security definer set search_path = public
as $$
declare
  node_row public.knowledge_nodes%rowtype;
  current_evidence public.mastery_evidence%rowtype;
  highest_evidence public.mastery_evidence%rowtype;
begin
  select * into node_row from public.knowledge_nodes where id = target_knowledge_node_id;
  if node_row.id is null then return; end if;

  select evidence.* into current_evidence
  from public.mastery_evidence evidence
  where evidence.knowledge_node_id = target_knowledge_node_id
    and not exists (
      select 1 from public.mastery_evidence_revocations revocation
      where revocation.evidence_id = evidence.id
    )
  order by evidence.created_at desc, evidence.id desc
  limit 1;

  select evidence.* into highest_evidence
  from public.mastery_evidence evidence
  where evidence.knowledge_node_id = target_knowledge_node_id
    and not exists (
      select 1 from public.mastery_evidence_revocations revocation
      where revocation.evidence_id = evidence.id
    )
  order by case evidence.level
      when 'mastered' then 4
      when 'basic' then 3
      when 'practiced' then 2
      when 'reinforce' then 1
      else 0 end desc,
    evidence.created_at desc, evidence.id desc
  limit 1;

  insert into public.mastery_snapshots(
    student_id, knowledge_node_id, subject_id, current_level, highest_level,
    current_evidence_id, latest_evidence_at, highest_achieved_at
  ) values (
    node_row.student_id, node_row.id, node_row.subject_id,
    coalesce(current_evidence.level, 'unpracticed'::public.mastery_level),
    coalesce(highest_evidence.level, 'unpracticed'::public.mastery_level),
    current_evidence.id, current_evidence.created_at, highest_evidence.created_at
  )
  on conflict (student_id, knowledge_node_id) do update set
    current_level = excluded.current_level,
    highest_level = excluded.highest_level,
    current_evidence_id = excluded.current_evidence_id,
    latest_evidence_at = excluded.latest_evidence_at,
    highest_achieved_at = excluded.highest_achieved_at,
    version = public.mastery_snapshots.version + 1,
    updated_at = now();
end;
$$;

create or replace function public.refresh_task_workflow(target_task_id uuid)
returns public.workflow_stage
language plpgsql security definer set search_path = public
as $$
declare
  task_row public.homework_tasks%rowtype;
  activity_row public.student_task_activity%rowtype;
  review_row public.task_review_records%rowtype;
  correction_row public.correction_attempts%rowtype;
  next_stage public.workflow_stage;
  has_mastery boolean;
  submissions_complete boolean;
begin
  select * into task_row from public.homework_tasks where id = target_task_id;
  if task_row.id is null then raise exception 'task not found'; end if;

  select * into activity_row from public.student_task_activity where task_id = target_task_id;
  if activity_row.task_id is null or activity_row.run_state = 'ready' then
    next_stage := 'ready';
  elsif activity_row.run_state in ('running', 'paused') then
    next_stage := 'in_progress';
  else
    select review.* into review_row
    from public.task_review_records review
    where review.task_id = target_task_id
      and not exists (select 1 from public.task_review_revocations revoked where revoked.review_id = review.id)
    order by review.review_number desc limit 1;

    if review_row.id is null then
      next_stage := 'awaiting_review';
    else
      select correction.* into correction_row
      from public.correction_attempts correction
      where correction.review_id = review_row.id
        and not exists (
          select 1 from public.correction_attempt_revocations revoked
          where revoked.correction_attempt_id = correction.id
        )
      order by correction.attempt_number desc limit 1;

      if review_row.correction_required and (correction_row.id is null or not correction_row.correction_passed) then
        next_stage := 'awaiting_correction';
      elsif review_row.redo_required and (correction_row.id is null or not correction_row.redo_passed) then
        next_stage := 'awaiting_redo';
      else
        select exists (
          select 1 from public.task_knowledge_links link
          where link.task_id = target_task_id
        ) and not exists (
          select 1 from public.task_knowledge_links link
          where link.task_id = target_task_id
            and not exists (
              select 1 from public.mastery_evidence evidence
              where evidence.task_id = target_task_id
                and evidence.knowledge_node_id = link.knowledge_node_id
                and evidence.evidence_type = 'tutor_assessment'
                and evidence.level = 'mastered'
                and not exists (
                  select 1 from public.mastery_evidence_revocations revoked
                  where revoked.evidence_id = evidence.id
                )
            )
        ) into has_mastery;

        if not has_mastery then
          next_stage := 'awaiting_acceptance';
        else
          select not exists (
            select 1 from public.submission_checkpoints checkpoint
            where checkpoint.homework_id = task_row.homework_id and checkpoint.required
              and checkpoint.status <> 'confirmed'
          ) into submissions_complete;
          next_stage := case when submissions_complete then 'closed_loop' else 'awaiting_acceptance' end;
        end if;
      end if;
    end if;
  end if;

  insert into public.task_workflow_current(task_id, stage, updated_by)
  values(target_task_id, next_stage, auth.uid())
  on conflict (task_id) do update set
    stage = excluded.stage,
    version = public.task_workflow_current.version + 1,
    updated_by = coalesce(auth.uid(), public.task_workflow_current.updated_by),
    updated_at = now();
  return next_stage;
end;
$$;

create or replace function public.record_student_task_event(
  target_task_id uuid,
  target_event text,
  target_unknown_numbers text[],
  expected_version integer,
  target_idempotency_key uuid
)
returns public.task_workflow_current
language plpgsql security definer set search_path = public
as $$
declare
  task_row public.homework_tasks%rowtype;
  workflow_row public.task_workflow_current%rowtype;
  elapsed integer := 0;
  normalized_unknowns text[] := '{}';
  logged_event text;
  node_id uuid;
begin
  if auth.uid() is null then raise exception 'authentication required'; end if;
  if target_idempotency_key is null then raise exception 'idempotency key required'; end if;
  if target_event not in ('started', 'paused', 'completed') then raise exception 'invalid student event'; end if;

  if exists (
    select 1 from public.study_session_events
    where actor_id = auth.uid() and idempotency_key = target_idempotency_key
  ) then
    select * into workflow_row from public.task_workflow_current where task_id = target_task_id;
    return workflow_row;
  end if;

  select * into task_row from public.homework_tasks where id = target_task_id and deleted_at is null;
  if task_row.id is null or not public.is_student_owner(task_row.student_id) then raise exception 'student access required'; end if;
  select * into workflow_row from public.task_workflow_current where task_id = target_task_id for update;
  if workflow_row.task_id is null then raise exception 'workflow not initialized'; end if;
  if workflow_row.version <> expected_version then raise exception 'version conflict'; end if;

  select coalesce(array_agg(value order by ordinal), '{}') into normalized_unknowns
  from (
    select trim(raw_value) value, min(ordinal) ordinal
    from unnest(coalesce(target_unknown_numbers, '{}')) with ordinality input(raw_value, ordinal)
    where nullif(trim(raw_value), '') is not null
    group by trim(raw_value)
  ) normalized;

  if target_event = 'started' then
    if workflow_row.stage not in ('ready', 'in_progress') then raise exception 'task cannot be started from current stage'; end if;
    if workflow_row.active_started_at is not null then raise exception 'task already running'; end if;
    logged_event := case when workflow_row.stage = 'in_progress' then 'resumed' else 'started' end;
    update public.task_workflow_current set active_started_at = now() where task_id = target_task_id;
    insert into public.student_task_activity(task_id, student_id, run_state, unknown_numbers, started_at)
    values(target_task_id, task_row.student_id, 'running', normalized_unknowns, now())
    on conflict (task_id) do update set
      run_state = 'running', unknown_numbers = excluded.unknown_numbers,
      started_at = coalesce(public.student_task_activity.started_at, excluded.started_at), updated_at = now();
  elsif target_event = 'paused' then
    if workflow_row.stage <> 'in_progress' or workflow_row.active_started_at is null then raise exception 'task is not running'; end if;
    elapsed := greatest(0, floor(extract(epoch from (now() - workflow_row.active_started_at)))::integer);
    logged_event := 'paused';
    update public.task_workflow_current
    set actual_seconds = actual_seconds + elapsed, active_started_at = null
    where task_id = target_task_id;
    update public.student_task_activity
    set run_state = 'paused', unknown_numbers = normalized_unknowns, updated_at = now()
    where task_id = target_task_id;
  else
    if workflow_row.stage <> 'in_progress' then raise exception 'task is not in progress'; end if;
    if workflow_row.active_started_at is not null then
      elapsed := greatest(0, floor(extract(epoch from (now() - workflow_row.active_started_at)))::integer);
    end if;
    logged_event := 'completed';
    update public.task_workflow_current
    set actual_seconds = actual_seconds + elapsed, active_started_at = null, last_completed_at = now()
    where task_id = target_task_id;
    update public.student_task_activity
    set run_state = 'completed', unknown_numbers = normalized_unknowns,
      completed_at = now(), updated_at = now()
    where task_id = target_task_id;

    for node_id in
      select link.knowledge_node_id
      from public.task_knowledge_links link
      where link.task_id = task_row.id
    loop
      insert into public.mastery_evidence(
        student_id, subject_id, knowledge_node_id, task_id, homework_version_id,
        evidence_type, level, detail, created_by
      ) values (
        task_row.student_id, task_row.subject_id, node_id, task_row.id,
        task_row.homework_version_id, 'first_attempt', 'practiced',
        jsonb_build_object('unknown_numbers', normalized_unknowns), auth.uid()
      );
      perform public.recalculate_mastery_snapshot(node_id);
    end loop;
  end if;

  insert into public.study_session_events(
    task_id, student_id, event_type, unknown_numbers, elapsed_seconds,
    actor_id, idempotency_key
  ) values (
    target_task_id, task_row.student_id, logged_event, normalized_unknowns,
    elapsed, auth.uid(), target_idempotency_key
  );

  perform public.refresh_task_workflow(target_task_id);
  insert into public.change_events(
    family_id, student_id, subject_id, entity_type, entity_id, event_type,
    after_value, actor_id, idempotency_key
  ) values (
    task_row.family_id, task_row.student_id, task_row.subject_id, 'task',
    task_row.id::text, 'student_' || logged_event,
    jsonb_build_object('unknown_numbers', normalized_unknowns, 'elapsed_seconds', elapsed),
    auth.uid(), target_idempotency_key
  );
  if logged_event = 'completed' then
    perform public.notify_task_audience(target_task_id, 'tutor', 'task_awaiting_review', '孩子已完成，等待批改', task_row.title);
  end if;
  select * into workflow_row from public.task_workflow_current where task_id = target_task_id;
  return workflow_row;
end;
$$;

create or replace function public.move_homework_blocks(
  target_task_id uuid,
  target_date date,
  change_reason text,
  expected_version integer,
  move_following boolean,
  target_idempotency_key uuid
)
returns integer
language plpgsql security definer set search_path = public
as $$
declare
  root_task public.homework_tasks%rowtype;
  affected_id uuid;
  affected_old_date date;
  date_delta integer;
  moved_count integer := 0;
begin
  if auth.uid() is null then raise exception 'authentication required'; end if;
  if target_date is null then raise exception 'target date required'; end if;
  if nullif(trim(change_reason), '') is null then raise exception 'change reason required'; end if;
  if target_idempotency_key is null then raise exception 'idempotency key required'; end if;
  if exists (select 1 from public.change_events where actor_id = auth.uid() and idempotency_key = target_idempotency_key) then
    select coalesce((after_value ->> 'moved_count')::integer, 0) into moved_count
    from public.change_events where actor_id = auth.uid() and idempotency_key = target_idempotency_key;
    return moved_count;
  end if;

  select * into root_task from public.homework_tasks where id = target_task_id and deleted_at is null for update;
  if root_task.id is null or not public.is_task_tutor(root_task.id) then raise exception 'subject tutor access required'; end if;
  if root_task.version <> expected_version then raise exception 'version conflict'; end if;
  date_delta := target_date - root_task.planned_date;
  if date_delta = 0 then return 0; end if;

  for affected_id, affected_old_date in
    with recursive affected(task_id) as (
      select root_task.id
      union
      select dependency.successor_task_id
      from public.plan_dependencies dependency
      join affected on affected.task_id = dependency.predecessor_task_id
      join public.homework_tasks successor on successor.id = dependency.successor_task_id
      where move_following and successor.deleted_at is null
        and successor.student_id = root_task.student_id and successor.subject_id = root_task.subject_id
    )
    select task.id, task.planned_date
    from affected join public.homework_tasks task on task.id = affected.task_id
    order by task.sequence_number, task.id
  loop
    update public.homework_tasks
    set planned_date = affected_old_date + date_delta,
      version = version + 1, updated_by = auth.uid()
    where id = affected_id;
    insert into public.task_plan_changes(task_id, old_date, new_date, reason, changed_by)
    values(affected_id, affected_old_date, affected_old_date + date_delta, trim(change_reason), auth.uid());
    moved_count := moved_count + 1;
  end loop;

  insert into public.change_events(
    family_id, student_id, subject_id, entity_type, entity_id, event_type,
    before_value, after_value, reason, actor_id, idempotency_key
  ) values (
    root_task.family_id, root_task.student_id, root_task.subject_id, 'task',
    root_task.id::text, case when move_following then 'plan_chain_moved' else 'plan_block_moved' end,
    jsonb_build_object('planned_date', root_task.planned_date),
    jsonb_build_object('planned_date', target_date, 'moved_count', moved_count),
    trim(change_reason), auth.uid(), target_idempotency_key
  );
  perform public.notify_task_audience(root_task.id, 'parent', 'plan_changed', '家教已调整本科计划', trim(change_reason));
  return moved_count;
end;
$$;

