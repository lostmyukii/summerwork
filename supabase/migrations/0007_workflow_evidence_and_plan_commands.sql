create or replace function public.merge_homework_blocks(
  target_first_task_id uuid,
  target_second_task_id uuid,
  first_expected_version integer,
  second_expected_version integer,
  change_reason text,
  target_idempotency_key uuid
)
returns uuid
language plpgsql security definer set search_path = public
as $$
declare
  first_task public.homework_tasks%rowtype;
  second_task public.homework_tasks%rowtype;
  first_workflow public.task_workflow_current%rowtype;
  second_workflow public.task_workflow_current%rowtype;
  merged_minutes integer;
begin
  if auth.uid() is null then raise exception 'authentication required'; end if;
  if target_first_task_id = target_second_task_id then raise exception 'two distinct blocks required'; end if;
  if nullif(trim(change_reason), '') is null then raise exception 'change reason required'; end if;
  if target_idempotency_key is null then raise exception 'idempotency key required'; end if;
  if exists (select 1 from public.change_events where actor_id = auth.uid() and idempotency_key = target_idempotency_key) then return target_first_task_id; end if;

  select * into first_task from public.homework_tasks where id = target_first_task_id and deleted_at is null for update;
  select * into second_task from public.homework_tasks where id = target_second_task_id and deleted_at is null for update;
  if first_task.id is null or second_task.id is null then raise exception 'block not found'; end if;
  if first_task.homework_id is distinct from second_task.homework_id then raise exception 'blocks must belong to one homework'; end if;
  if not public.is_task_tutor(first_task.id) or not public.is_task_tutor(second_task.id) then raise exception 'subject tutor access required'; end if;
  if first_task.version <> first_expected_version or second_task.version <> second_expected_version then raise exception 'version conflict'; end if;
  select * into first_workflow from public.task_workflow_current where task_id = first_task.id for update;
  select * into second_workflow from public.task_workflow_current where task_id = second_task.id for update;
  if first_workflow.stage <> 'ready' or second_workflow.stage <> 'ready'
    or exists (select 1 from public.study_session_events where task_id in (first_task.id, second_task.id))
  then raise exception 'only unstarted blocks can be merged'; end if;
  merged_minutes := first_task.block_minutes + second_task.block_minutes;
  if merged_minutes > 360 then raise exception 'merged duration exceeds limit'; end if;

  insert into public.plan_dependencies(predecessor_task_id, successor_task_id, dependency_type, created_by)
  select first_task.id, dependency.successor_task_id, dependency.dependency_type, auth.uid()
  from public.plan_dependencies dependency
  where dependency.predecessor_task_id = second_task.id and dependency.successor_task_id <> first_task.id
  on conflict do nothing;
  insert into public.plan_dependencies(predecessor_task_id, successor_task_id, dependency_type, created_by)
  select dependency.predecessor_task_id, first_task.id, dependency.dependency_type, auth.uid()
  from public.plan_dependencies dependency
  where dependency.successor_task_id = second_task.id and dependency.predecessor_task_id <> first_task.id
  on conflict do nothing;
  delete from public.plan_dependencies
  where predecessor_task_id = second_task.id or successor_task_id = second_task.id
    or (predecessor_task_id = first_task.id and successor_task_id = second_task.id)
    or (predecessor_task_id = second_task.id and successor_task_id = first_task.id);

  update public.homework_tasks
  set block_minutes = merged_minutes, recommended_minutes = merged_minutes,
    version = version + 1, updated_by = auth.uid()
  where id = first_task.id;
  update public.homework_tasks
  set deleted_at = now(), deleted_by = auth.uid(), version = version + 1,
    updated_by = auth.uid()
  where id = second_task.id;

  insert into public.change_events(
    family_id, student_id, subject_id, entity_type, entity_id, event_type,
    before_value, after_value, reason, actor_id, idempotency_key
  ) values (
    first_task.family_id, first_task.student_id, first_task.subject_id, 'task',
    first_task.id::text, 'plan_blocks_merged',
    jsonb_build_object('first_minutes', first_task.block_minutes, 'second_task_id', second_task.id, 'second_minutes', second_task.block_minutes),
    jsonb_build_object('task_id', first_task.id, 'minutes', merged_minutes),
    trim(change_reason), auth.uid(), target_idempotency_key
  );
  perform public.notify_task_audience(first_task.id, 'parent', 'plan_changed', '家教已合并任务块', trim(change_reason));
  return first_task.id;
end;
$$;

create or replace function public.save_task_review(
  target_task_id uuid,
  target_accuracy_band text,
  target_wrong_numbers text[],
  target_error_tags text[],
  target_correction_required boolean,
  target_redo_required boolean,
  target_note text,
  expected_version integer,
  target_idempotency_key uuid
)
returns uuid
language plpgsql security definer set search_path = public
as $$
declare
  task_row public.homework_tasks%rowtype;
  workflow_row public.task_workflow_current%rowtype;
  review_id uuid;
  next_review_number integer;
  normalized_wrongs text[] := '{}';
  normalized_tags text[] := '{}';
  node_id uuid;
  evidence_level public.mastery_level;
begin
  if auth.uid() is null then raise exception 'authentication required'; end if;
  if target_idempotency_key is null then raise exception 'idempotency key required'; end if;
  if target_accuracy_band not in ('100', '90+', '70-89', 'below-70') then raise exception 'invalid accuracy band'; end if;
  if exists (select 1 from public.change_events where actor_id = auth.uid() and idempotency_key = target_idempotency_key) then
    select (after_value ->> 'review_id')::uuid into review_id
    from public.change_events where actor_id = auth.uid() and idempotency_key = target_idempotency_key;
    return review_id;
  end if;

  select * into task_row from public.homework_tasks where id = target_task_id and deleted_at is null;
  if task_row.id is null or not public.is_task_tutor(task_row.id) then raise exception 'subject tutor access required'; end if;
  select * into workflow_row from public.task_workflow_current where task_id = target_task_id for update;
  if workflow_row.version <> expected_version then raise exception 'version conflict'; end if;
  if workflow_row.stage <> 'awaiting_review' then raise exception 'task is not awaiting review'; end if;

  select coalesce(array_agg(value order by ordinal), '{}') into normalized_wrongs
  from (
    select trim(raw_value) value, min(ordinal) ordinal
    from unnest(coalesce(target_wrong_numbers, '{}')) with ordinality input(raw_value, ordinal)
    where nullif(trim(raw_value), '') is not null
    group by trim(raw_value)
  ) normalized;
  select coalesce(array_agg(value order by ordinal), '{}') into normalized_tags
  from (
    select trim(raw_value) value, min(ordinal) ordinal
    from unnest(coalesce(target_error_tags, '{}')) with ordinality input(raw_value, ordinal)
    where nullif(trim(raw_value), '') is not null
    group by trim(raw_value)
  ) normalized;
  if cardinality(normalized_wrongs) > 0 and not target_correction_required then raise exception 'wrong answers require correction'; end if;
  if target_redo_required and not target_correction_required then raise exception 'redo requires correction'; end if;
  if exists (
    select 1 from public.task_review_records review
    where review.task_id = target_task_id
      and not exists (select 1 from public.task_review_revocations revoked where revoked.review_id = review.id)
  ) then raise exception 'active review already exists'; end if;

  select coalesce(max(review_number), 0) + 1 into next_review_number
  from public.task_review_records where task_id = target_task_id;
  insert into public.task_review_records(
    task_id, homework_version_id, review_number, accuracy_band, wrong_numbers,
    error_tags, correction_required, redo_required, note, reviewed_by, idempotency_key
  ) values (
    target_task_id, task_row.homework_version_id, next_review_number,
    target_accuracy_band, normalized_wrongs, normalized_tags,
    target_correction_required, target_redo_required, coalesce(target_note, ''),
    auth.uid(), target_idempotency_key
  ) returning id into review_id;

  evidence_level := case when cardinality(normalized_wrongs) > 0 then 'reinforce'::public.mastery_level else 'practiced'::public.mastery_level end;
  for node_id in
    select link.knowledge_node_id from public.task_knowledge_links link
    where link.task_id = task_row.id
  loop
    insert into public.mastery_evidence(
      student_id, subject_id, knowledge_node_id, task_id, homework_version_id,
      review_id, evidence_type, level, detail, created_by
    ) values (
      task_row.student_id, task_row.subject_id, node_id, task_row.id,
      task_row.homework_version_id, review_id,
      case when cardinality(normalized_wrongs) > 0 then 'wrong_answer' else 'tutor_assessment' end,
      evidence_level,
      jsonb_build_object('accuracy_band', target_accuracy_band, 'wrong_numbers', normalized_wrongs, 'error_tags', normalized_tags),
      auth.uid()
    );
    perform public.recalculate_mastery_snapshot(node_id);
  end loop;

  insert into public.task_reviews(
    task_id, reviewed_by, accuracy_band, wrong_numbers, error_tags,
    correction_passed, redo_required, redo_passed, mastery_confirmed,
    review_confirmed_at, review_saved_at, note
  ) values (
    target_task_id, auth.uid(), target_accuracy_band, normalized_wrongs,
    normalized_tags, false, target_redo_required, false, false, now(), now(),
    coalesce(target_note, '')
  )
  on conflict (task_id) do update set
    reviewed_by = excluded.reviewed_by, accuracy_band = excluded.accuracy_band,
    wrong_numbers = excluded.wrong_numbers, error_tags = excluded.error_tags,
    correction_passed = false, redo_required = excluded.redo_required,
    redo_passed = false, mastery_confirmed = false,
    review_confirmed_at = excluded.review_confirmed_at,
    review_saved_at = excluded.review_saved_at, note = excluded.note, updated_at = now();

  perform public.refresh_task_workflow(target_task_id);
  insert into public.change_events(
    family_id, student_id, subject_id, entity_type, entity_id, event_type,
    after_value, actor_id, idempotency_key
  ) values (
    task_row.family_id, task_row.student_id, task_row.subject_id, 'review',
    review_id::text, 'review_confirmed',
    jsonb_build_object('review_id', review_id, 'accuracy_band', target_accuracy_band,
      'wrong_count', cardinality(normalized_wrongs), 'correction_required', target_correction_required,
      'redo_required', target_redo_required),
    auth.uid(), target_idempotency_key
  );
  perform public.notify_task_audience(target_task_id, 'student',
    case when target_correction_required then 'correction_required' else 'mastery_pending' end,
    case when target_correction_required then '批改完成，请按错题订正' else '批改完成' end,
    task_row.title);
  return review_id;
end;
$$;

create or replace function public.revoke_task_review(
  target_task_id uuid,
  target_revoke_reason text,
  expected_version integer,
  target_idempotency_key uuid
)
returns void
language plpgsql security definer set search_path = public
as $$
declare
  task_row public.homework_tasks%rowtype;
  workflow_row public.task_workflow_current%rowtype;
  review_row public.task_review_records%rowtype;
  evidence_row record;
begin
  if auth.uid() is null then raise exception 'authentication required'; end if;
  if nullif(trim(target_revoke_reason), '') is null then raise exception 'revoke reason required'; end if;
  if target_idempotency_key is null then raise exception 'idempotency key required'; end if;
  if exists (select 1 from public.change_events where actor_id = auth.uid() and idempotency_key = target_idempotency_key) then return; end if;
  select * into task_row from public.homework_tasks where id = target_task_id and deleted_at is null;
  if task_row.id is null or not public.is_task_tutor(task_row.id) then raise exception 'subject tutor access required'; end if;
  select * into workflow_row from public.task_workflow_current where task_id = target_task_id for update;
  if workflow_row.version <> expected_version then raise exception 'version conflict'; end if;
  select review.* into review_row from public.task_review_records review
  where review.task_id = target_task_id
    and not exists (select 1 from public.task_review_revocations revoked where revoked.review_id = review.id)
  order by review.review_number desc limit 1;
  if review_row.id is null then raise exception 'active review not found'; end if;

  insert into public.task_review_revocations(review_id, reason, revoked_by, idempotency_key)
  values(review_row.id, trim(target_revoke_reason), auth.uid(), target_idempotency_key);
  for evidence_row in
    select evidence.id, evidence.knowledge_node_id from public.mastery_evidence evidence
    where evidence.review_id = review_row.id
      and not exists (select 1 from public.mastery_evidence_revocations revoked where revoked.evidence_id = evidence.id)
  loop
    insert into public.mastery_evidence_revocations(evidence_id, reason, revoked_by)
    values(evidence_row.id, trim(target_revoke_reason), auth.uid());
    perform public.recalculate_mastery_snapshot(evidence_row.knowledge_node_id);
  end loop;
  delete from public.task_reviews where task_id = target_task_id;
  perform public.refresh_task_workflow(target_task_id);
  insert into public.change_events(
    family_id, student_id, subject_id, entity_type, entity_id, event_type,
    before_value, reason, actor_id, idempotency_key
  ) values (
    task_row.family_id, task_row.student_id, task_row.subject_id, 'review',
    review_row.id::text, 'review_revoked', jsonb_build_object('review_id', review_row.id),
    trim(target_revoke_reason), auth.uid(), target_idempotency_key
  );
end;
$$;

