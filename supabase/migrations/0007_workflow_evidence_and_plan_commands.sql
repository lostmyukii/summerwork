  elsif target_audience = 'tutor' then
    insert into public.notifications(family_id, student_id, subject_id, recipient_id, notification_type, title, body, entity_type, entity_id)
    select task_row.family_id, task_row.student_id, task_row.subject_id, assignment.tutor_user_id,
      target_type, target_title, coalesce(target_body, ''), 'task', task_row.id::text
    from public.tutor_assignments assignment
