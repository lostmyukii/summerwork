create or replace function public.notify_task_audience(
  target_task_id uuid,
  target_audience text,
  target_type text,
  target_title text,
  target_body text default ''
)
returns void
language plpgsql security definer set search_path = public
as $$
declare task_row public.homework_tasks%rowtype;
begin
  select * into task_row from public.homework_tasks where id = target_task_id;
  if task_row.id is null then return; end if;

  if target_audience = 'parent' then
    insert into public.notifications(family_id, student_id, subject_id, recipient_id, notification_type, title, body, entity_type, entity_id)
    select task_row.family_id, task_row.student_id, task_row.subject_id, membership.user_id,
